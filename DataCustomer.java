    private void ButtonSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonSearchActionPerformed
       String cari = Cari.getText().trim(); // Ambil teks dari field Cari (misalnya field untuk nama)

    // Validasi jika cari kosong
    if (cari.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Masukkan nama yang ingin dicari!", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    try {
        // Koneksi ke database
        Connection conn = GUI.mycon();
        
        // Query SQL untuk mencari data berdasarkan ID
        String sql = "SELECT * FROM customer WHERE id_customer LIKE ?";
        PreparedStatement ps = conn.prepareStatement(sql);
        ps.setString(1, "%" + cari + "%");  // Gunakan LIKE dengan wildcard untuk mencari ID yang mengandung kata kunci

        // Eksekusi query
        ResultSet rs = ps.executeQuery();

        // Cek apakah ada data yang ditemukan
        if (rs.next()) {
            // Jika ditemukan, tampilkan data pada form input
            Nama.setText(rs.getString("nama"));
            Kontak.setText(rs.getString("kontak"));
            Alamat.setText(rs.getString("alamat"));
            Tanggal.setText(rs.getString("tanggal_bergabung"));
            String jenis_customer = rs.getString("jenis_customer");
            if (jenis_customer != null) {
                // Pastikan status yang ada di database dipilih di combo box
                Jenis.setSelectedItem(jenis_customer);  // Menyesuaikan dengan nilai status dari database (misalnya "Aktif" atau "Tidak Aktif")
            }
            // Set Status combo box dengan nilai status dari database
            String status = rs.getString("status");
            if (status != null) {
                // Pastikan status yang ada di database dipilih di combo box
                Status.setSelectedItem(status);  // Menyesuaikan dengan nilai status dari database (misalnya "Aktif" atau "Tidak Aktif")
            }
            

        } else {
            // Jika tidak ada hasil pencarian
            JOptionPane.showMessageDialog(this, "Data tidak ditemukan!", "Pencarian Gagal", JOptionPane.INFORMATION_MESSAGE);
        }

        // Tutup ResultSet, PreparedStatement, dan koneksi
        rs.close();
        ps.close();
        
    } catch (Exception e) {
        // Tangani error koneksi atau lainnya
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage(), "Exception", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_ButtonSearchActionPerformed

