    private void ButtonUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonUpdateActionPerformed
        String nama = Nama.getText(); // Nama customer
    String kontak = Kontak.getText(); // Kontak customer
    String alamat = Alamat.getText(); // Alamat customer
    String tanggalBergabung = Tanggal.getText(); // Tanggal bergabung customer
    String jenisCustomer = Jenis.getSelectedItem().toString(); // Jenis customer
    String status = Status.getSelectedItem().toString(); // Status customer

        if (nama.isEmpty() || kontak.isEmpty() || alamat.isEmpty() || jenisCustomer.isEmpty() || status.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Semua field harus diisi!", "Error", JOptionPane.ERROR_MESSAGE);
            return; 
        }

        try {
            String cari = Cari.getText().trim();

        if (cari.isEmpty()) {
            JOptionPane.showMessageDialog(this, "ID Customer tidak boleh kosong!", "Error", JOptionPane.ERROR_MESSAGE);
            return; // Berhenti jika ID customer kosong
        }

        // Koneksi ke database
        Connection conn = GUI.mycon();
        
        // Query untuk update data berdasarkan ID
        String sql = """
            UPDATE customer 
            SET 
                nama = ?, 
                kontak = ?, 
                alamat = ?, 
                tanggal_bergabung = ?,
                jenis_customer = ?, 
                status = ? 
            WHERE id_customer = ?
        """;
        PreparedStatement ps = conn.prepareStatement(sql);
        
        // Set parameter pada query update
        ps.setString(1, nama);
        ps.setString(2, kontak);
        ps.setString(3, alamat);
        ps.setDate(4, java.sql.Date.valueOf(tanggalBergabung)); // Konversi tanggal ke SQL Date
        ps.setString(5, jenisCustomer);
        ps.setString(6, status);
        ps.setString(7, cari); // ID customer sebagai patokan untuk update

        // Eksekusi query update
        int rowsAffected = ps.executeUpdate();
        
        if (rowsAffected > 0) {
            // Data berhasil diupdate
            JOptionPane.showMessageDialog(this, "Data berhasil diupdate", "Update Sukses", JOptionPane.INFORMATION_MESSAGE);
        } else {
            // Data gagal diupdate
            JOptionPane.showMessageDialog(this, "Data gagal diupdate. Pastikan ID Customer benar.", "Update Gagal", JOptionPane.ERROR_MESSAGE);
        }

        // Tutup PreparedStatement
        ps.close();

        // Refresh tampilan data jika ada metode loadData()
        loadData();

    } catch (Exception e) {
        // Tangani error koneksi atau lainnya
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage(), "Exception", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_ButtonUpdateActionPerformed

    private void ButtonSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonSaveActionPerformed
        String nama = Nama.getText(); // Nama customer
    String kontak = Kontak.getText(); // Kontak customer
    String alamat = Alamat.getText(); // Alamat customer
    String tanggalBergabung = Tanggal.getText(); // Tanggal bergabung customer
    String jenisCustomer = Jenis.getSelectedItem().toString(); // Jenis customer
    String status = Status.getSelectedItem().toString(); // Status customer

    // Validasi input kosong
    if (nama.isEmpty() || kontak.isEmpty() || alamat.isEmpty() || tanggalBergabung.isEmpty() || 
        jenisCustomer.isEmpty() || status.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Semua field harus diisi!", "Error", JOptionPane.ERROR_MESSAGE);
        return; // Hentikan proses jika ada field kosong
    }

    try {
        // Ambil koneksi dari GUI.java
        Connection conn = GUI.mycon();

        // Query untuk insert data
        String sql = """
            INSERT INTO customer (nama, kontak, alamat, tanggal_bergabung, jenis_customer, status)
            VALUES (?, ?, ?, ?, ?, ?)
        """;
        PreparedStatement ps = conn.prepareStatement(sql);

        // Set nilai dari input GUI ke query
        ps.setString(1, nama);
        ps.setString(2, kontak);
        ps.setString(3, alamat);
        ps.setDate(4, java.sql.Date.valueOf(tanggalBergabung)); // Konversi tanggal ke SQL Date
        ps.setString(5, jenisCustomer);
        ps.setString(6, status);

        // Eksekusi query
        ps.executeUpdate();

        // Beri notifikasi berhasil
        JOptionPane.showMessageDialog(this, "Data customer berhasil disimpan!", "Sukses", JOptionPane.INFORMATION_MESSAGE);

        // Reset input GUI setelah berhasil insert
        Nama.setText("");
        Kontak.setText("");
        Alamat.setText("");
        Tanggal.setText(""); // Kosongkan field tanggal
        Status.setSelectedIndex(0); // Reset ke pilihan pertama
        Status.setSelectedIndex(0); // Reset ke pilihan pertama

        loadData(); // Refresh data di tabel

        // Tutup PreparedStatement
        ps.close();

    } catch (Exception e) {
        // Tangani error
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_ButtonSaveActionPerformed

