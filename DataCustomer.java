    private void loadTransaksi(){
        DefaultTableModel customerModel = (DefaultTableModel) TabelTransaksiCustomer.getModel();

        customerModel.setRowCount(0);

        try {

            Connection conn = GUI.mycon();

            String sql = "SELECT bk.id_penjualan, bk.tanggal_keluar, bk.id_barang, bk.jumlah, bk.pendapatan, c.nama AS nama_customer, bk.status " +
                 "FROM barang_keluar bk " +
                 "JOIN customer c ON bk.id_customer = c.id_customer " +
                 "WHERE c.status = 'aktif'";
            PreparedStatement ps = conn.prepareStatement(sql);
            ResultSet rs = ps.executeQuery();

            while (rs.next()) {
                customerModel.addRow(new Object[]{
                rs.getInt("id_penjualan"),         
                rs.getDate("tanggal_keluar"),    
                rs.getString("id_barang"),         
                rs.getInt("jumlah"),             
                rs.getDouble("pendapatan"),        
                rs.getString("nama_customer"),  
                rs.getString("status")           
            });
        }

        rs.close();
        ps.close();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal memuat data transaksi customer: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
    
    private void loadData() {
        DefaultTableModel model = (DefaultTableModel) TabelCustomer.getModel();
    
        model.setRowCount(0);

        try {
            Connection conn = GUI.mycon();
            String sql = "SELECT * FROM customer";
            PreparedStatement ps = conn.prepareStatement(sql);
            ResultSet rs = ps.executeQuery();

            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getInt("id_customer"),
                    rs.getString("nama"),
                    rs.getString("kontak"),
                    rs.getString("alamat"),
                    rs.getDate("tanggal_bergabung"),
                    rs.getString("jenis_customer"),
                    rs.getString("status")
              
                });
            }

        rs.close();
        ps.close();
       
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal memuat data: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }


