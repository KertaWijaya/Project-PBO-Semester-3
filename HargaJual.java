    private void ButtonSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonSaveActionPerformed
        // TODO add your handling code here:
        // Ambil nilai input dari GUI
    String idBarang = getSelectedBarangId(); // ID Barang dari ComboBox
    String hargaUnit = HargaUnit.getText(); // Harga per unit
    String hargaGrosir = HargaGrosir.getText(); // Harga grosir

    // Validasi input kosong
    if (idBarang == null || hargaUnit.isEmpty() || hargaGrosir.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Semua field harus diisi!", "Error", JOptionPane.ERROR_MESSAGE);
        return; // Hentikan proses jika ada field kosong
    }

    try {
        // Koneksi ke database
        Connection conn = GUI.mycon();

        // Query untuk insert data ke tabel hargajual
        String sql = """
            INSERT INTO hargajual (id_barang, hargaunit, hargagrosir)
            VALUES (?, ?, ?)
        """;
        PreparedStatement ps = conn.prepareStatement(sql);

        // Set nilai dari input GUI ke query
        ps.setString(1, idBarang);
        ps.setBigDecimal(2, new BigDecimal(hargaUnit)); // Konversi ke BigDecimal
        ps.setBigDecimal(3, new BigDecimal(hargaGrosir)); // Konversi ke BigDecimal

        // Eksekusi query
        ps.executeUpdate();

        // Beri notifikasi berhasil
        JOptionPane.showMessageDialog(this, "Data harga jual berhasil disimpan!", "Sukses", JOptionPane.INFORMATION_MESSAGE);

        // Reset input GUI setelah berhasil insert
        HargaUnit.setText("");
        HargaGrosir.setText("");
        NamaBarang.setSelectedIndex(0); // Reset ComboBox ke pilihan pertama

        loadData(); // Muat ulang data di tabel

        // Tutup PreparedStatement
        ps.close();
    } catch (SQLException e) {
        // Tangani error SQL
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    } catch (NumberFormatException e) {
        // Tangani error format angka
        JOptionPane.showMessageDialog(this, "Harga harus berupa angka yang valid!", "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_ButtonSaveActionPerformed
    
 private String getSelectedBarangId() {
    String namaDipilih = (String) NamaBarang.getSelectedItem(); // Ambil nama barang yang dipilih
    String idBarang = null;

    try {
        Connection conn = GUI.mycon();
        String sql = "SELECT id_barang FROM barang_masuk WHERE nama_barang = ?"; // Cari ID berdasarkan nama barang di tabel barang_masuk
        PreparedStatement ps = conn.prepareStatement(sql);
        ps.setString(1, namaDipilih); // Menggunakan nama barang yang dipilih
        ResultSet rs = ps.executeQuery();

        if (rs.next()) {
            idBarang = rs.getString("id_barang"); // Ambil id_barang dari hasil query
        }

        rs.close();
        ps.close();
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }

    return idBarang; // Kembalikan id_barang
}
   
