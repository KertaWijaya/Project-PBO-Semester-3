    private void loadNamaBarang() {
    try {
        // Koneksi ke database
        Connection conn = GUI.mycon();
        
        
        String sql = """
        SELECT DISTINCT bm.nama_barang
        FROM barang_masuk bm
        LEFT JOIN hargajual hj ON bm.id_barang = hj.id_barang
        WHERE hj.id_barang IS NULL
    """;
        PreparedStatement ps = conn.prepareStatement(sql);
        ResultSet rs = ps.executeQuery();

        // Bersihkan data sebelumnya di ComboBox
        
        NamaBarang.removeAllItems();
        while (rs.next()) {
            NamaBarang.addItem(rs.getString("nama_barang"));
        }
        String sql1 = "SELECT DISTINCT nama_barang FROM barang_masuk"; 
        PreparedStatement ps1 = conn.prepareStatement(sql1);
        ResultSet rs1 = ps1.executeQuery();
        NamaBarang1.removeAllItems();
        while (rs1.next()) {
            NamaBarang1.addItem(rs1.getString("nama_barang"));
        }

        // Tambahkan data nama barang unik ke ComboBox
        

        rs.close();
        ps.close();
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Gagal memuat nama barang: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
}

